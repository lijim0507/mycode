/**
 * @brief  获取 BQ40Z80 使用的 I2C 传输接口实例
 * @note   STM32 平台默认使用硬件 I2C（HAL）。
 *         调用前需确保 HAL_I2C_Init() 已完成，I2C 句柄由 STM32_I2C_PORT_HANDLE 宏配置。
 *         如需改用软件 I2C，将此函数改为返回 swi2c_get_transport() 即可。
 */
const i2c_transport_t *bq40z80_port_get_i2c(void)
{
    return stm32_i2c_port_get_transport();
}


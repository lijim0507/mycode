#include "key.h"

/*
 * Linux 示例——可通过以下方式读取按键状态：
 * - /sys/class/gpio/ 文件接口
 * - libgpiod
 * - /dev/input/eventX
 * - 键盘事件
 * - 模拟按键（定时器 + 标志位）
 *
 * 此处以静态标志位模拟按键（适合单元测试或 CI 环境）。
 */

#include <time.h>

/****************************************************************************/
/*                              Macros                                      */
/****************************************************************************/

/****************************************************************************/
/*                          Static Simulated Key                            */
/****************************************************************************/

static uint8_t g_linux_key_state = 0; /* 0: 释放, 1: 按下 */

/****************************************************************************/
/*                      Port Read / Tick Functions                          */
/****************************************************************************/

static uint8_t linux_key_read(void)
{
    return g_linux_key_state;
}

static uint32_t linux_key_tick(void)
{
    struct timespec ts;
    clock_gettime(CLOCK_MONOTONIC, &ts);
    return (uint32_t)(ts.tv_sec * 1000 + ts.tv_nsec / 1000000);
}

/****************************************************************************/
/*                      Simulation Helpers                                  */
/****************************************************************************/

/**
 * @brief  模拟按键按下（测试用）
 */
void linux_key_simulate_press(void)
{
    g_linux_key_state = 1;
}

/**
 * @brief  模拟按键释放（测试用）
 */
void linux_key_simulate_release(void)
{
    g_linux_key_state = 0;
}


#include "key.h"

#include "driver/gpio.h"
#include "esp_timer.h"

/****************************************************************************/
/*                              Macros                                      */
/****************************************************************************/

/* ---------- 根据实际硬件修改以下配置 ---------- */

#define KEY0_GPIO          GPIO_NUM_0
#define KEY0_ACTIVE_LEVEL  0

/* --------------------------------------------- */

/****************************************************************************/
/*                      Port Read / Tick Functions                          */
/****************************************************************************/

static uint8_t esp32_key_read(void)
{
    /* 归一化：按下 = 1，释放 = 0 */
    return (gpio_get_level(KEY0_GPIO) == KEY0_ACTIVE_LEVEL) ? 1 : 0;
}

static uint32_t esp32_key_tick(void)
{
    return (uint32_t)(esp_timer_get_time() / 1000);
}

/****************************************************************************/
/*                      Port Init Example                                   */
/****************************************************************************/

/**
 * @brief  初始化 ESP32 按键 GPIO（由上层在 app 初始化阶段调用）
 * @note   此函数非 core 层接口，仅作为 port 层 GPIO 配置示例
 */
void esp32_key_port_init(void)
{
    gpio_config_t io_conf = {
        .pin_bit_mask = (1ULL << KEY0_GPIO),
        .mode         = GPIO_MODE_INPUT,
        .pull_up_en   = GPIO_PULLUP_ENABLE,
        .pull_down_en = GPIO_PULLDOWN_DISABLE,
        .intr_type    = GPIO_INTR_DISABLE,
    };
    gpio_config(&io_conf);
}


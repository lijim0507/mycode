/**
 * @brief  事件分发
 * @param  key   按键对象指针
 * @param  event 事件类型
 * @note   若 callback 已注册且 event != KEY_EVENT_NONE，则调用之
 */
void key_event_dispatch(key_t *key, key_event_t event)
{
    if (key && key->callback && event != KEY_EVENT_NONE) {
        key->callback(key, event);
    }
}


/**
 * @brief  消抖滤波处理
 * @param  key 按键对象指针
 * @return 1: 稳定电平发生变化, 0: 无变化
 * @note   采用"稳定时间消抖"算法：
 *         当 raw_level != stable_level 时，记录起始 tick；
 *         若差异持续 >= debounce_ms，则确认电平跳变，更新 stable_level；
 *         若中途 raw_level 恢复与 stable_level 一致，则重置消抖。
 *         无 delay，无 busy-wait，纯非阻塞轮询。
 */
int key_filter_process(key_t *key)
{
    uint32_t tick = key->get_tick();
    int      level_changed = 0;

    key->raw_level = key->read();

    if (key->raw_level != key->stable_level) {
        if (!key->debounce_active) {
            key->debounce_active = 1;
            key->debounce_tick   = tick;
        }

        if (tick - key->debounce_tick >= key->debounce_ms) {
            key->stable_level    = key->raw_level;
            key->debounce_active = 0;
            level_changed        = 1;
        }
    } else {
        key->debounce_active = 0;
    }

    return level_changed;
}

